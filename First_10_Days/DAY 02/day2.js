let a = 50;
let b = 25;

/* Activity 1: Arithmetic Operations */
console.log("a + b : ", a + b);
console.log("a - b : ", a - b);
console.log("a * b : ", a * b);
console.log("a / b : ", a / b);
console.log("a % b : ", a % b);

/*  Activity 2: Assignment Operators */
a += b;
console.log("a += b", a);
a -= b;
console.log("a -= b", a);

/* Activity 3: Comparison Operators */
console.log("a < b : ", a < b);
console.log("a > b : ", a > b);
console.log("a <= b : ", a <= b);
console.log("a >= b : ", a >= b);
console.log("a == b : ", a == b);
console.log("a === b : ", a === b);

/* Activity 4: Logical Operators */
console.log("a && b : ", a && b);
console.log("a || b : ", a || b);
console.log("!a : ", !a);

/* Activity 5: Ternary Operator */
console.log(
  "a > 0 ? positive : negetive - ans is : ",
  a > 0 ? "positive" : "negetive"
);
console.log(
  "-11 > 0 ? positive : negetive - ans is : ",
  -11 > 0 ? "positive" : "negetive"
);
